uint8_t tb(uint8_t word, uint8_t opm)
{
    return (P8(ipm_masks_5mm[5 * opm] & word)
          + P8(ipm_masks_5mm[5 * opm + 1] & word)
          + P8(ipm_masks_5mm[5 * opm + 2] & word)
          + P8(ipm_masks_5mm[5 * opm + 3] & word)
          + P8(ipm_masks_5mm[5 * opm + 4] & word)) >= 3;
}

// Classify using the 5 Mask Method
void classify_5mm()
{
    uint64_t sample_size = 0x004000000;

    uint32_t pt[4], pt0, pt1, pt2, pt3;
    uint32_t ip_2D, ip_67, ip_8E, ip_A3, ip_C4;
    uint32_t opp;
    uint64_t count = sample_size;

    for (uint64_t i = 0; i < sample_size; i++)
    {
        for (uint16_t j = 0; j < 4; j++)
        {
            pt[j] = rand_uint32();
        }

        pt0 = (pt[0] >> 24) & 0xFF;
        pt1 = (pt[1] >> 16) & 0xFF;
        pt2 = (pt[2] >> 8) & 0xFF;
        pt3 = (pt[3] >> 0) & 0xFF;

        // Analyse plaintext
        ip_2D = tb(pt0, 0x16) ^ tb(pt1, 0x3B) ^ tb(pt2, 0x2D) ^ tb(pt3, 0x2D); // ipms for 2D
        ip_67 = tb(pt0, 0x33) ^ tb(pt1, 0x54) ^ tb(pt2, 0x67) ^ tb(pt3, 0x67); // ipms for 67
        ip_8E = tb(pt0, 0x47) ^ tb(pt1, 0xC9) ^ tb(pt2, 0x8E) ^ tb(pt3, 0x8E); // ipms for 8E
        ip_A3 = tb(pt0, 0x51) ^ tb(pt1, 0xF2) ^ tb(pt2, 0xA3) ^ tb(pt3, 0xA3); // ipms for A3
        ip_C4 = tb(pt0, 0x62) ^ tb(pt1, 0xA6) ^ tb(pt2, 0xC4) ^ tb(pt3, 0xC4); // ipms for C4

        // Encrypt
        SubBytes(pt);
        ShiftRows(pt);
        MixColumns(pt);
        SubBytes(pt);

        // Analyse ciphertext
        opp = P8((pt[0] >> 24) & 0x01);

        // count -= ((ip_2D + ip_67 + ip_8E + ip_A3 + ip_C4) >= 3) ^ opp;
        count -= ip_67 ^ opp;
    }
    printResults(&count, 1, sample_size);
}

